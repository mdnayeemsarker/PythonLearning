#Python Math

#Built-in Math Functions
print('\nBuilt-in Math Functions')
x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

#The abs() function returns the absolute (positive) value of the specified number
print('\nabs function')
x = abs(-7.25)
print(x)

#The pow(x, y) function returns the value of x to the power of y (xy).
print('\npower function')
x = pow(4, 3)
print(x)

#The Math Module
print('\nThe Math Module')
import math
x = math.sqrt(64)
print(x)

#The math.ceil(), and the math.floor()
print('\nceil and floor')
x = math.ceil(1.4)
y = math.floor(1.4)
print(x)
print(y)


#The math.pi constant, returns the value of PI (3.14...):
print('\nthe math pi')
x = math.pi
print(x)