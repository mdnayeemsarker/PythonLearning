#Python - Add List Items

#Append Items
print("\nAppend Items")

mylist = ["apple", "banana", "cherry"]
mylist.append("orange")
print(mylist)


#Insert Items
print("\nInsert Items")
mylist.insert(1, "orange insert")
print(mylist)


#Extend List
tropical = ["mango", "pineapple", "papaya"]
mylist.extend(tropical)
print(mylist)