#Python - Set Methods 

#add = 	Adds an element to the set
print("\nadd method")

myset = {"apple", "banana", "cherry"}
myset.add("orange")
print(myset)


#clear = Removes all the elements from the set
print("\nclear")
fruits = {"apple", "banana", "cherry"}
fruits.clear()
print(fruits)


#copy = Returns a copy of the set
print("\ncopy")
fruits = {"apple", "banana", "cherry"}
x = fruits.copy()
print(x)