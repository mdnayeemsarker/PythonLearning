#Python For Loops

#Python has two primitive loop commands:
  # while loops
  # for loops

#Python For Loops
print("\nPython For Loops")
fruits = ["apple", "banana", "cherry"]
for x in fruits:
  print(x)


#Looping Through a String
print("\nLooping Through a String")
for x in "banana":
  print(x)


#The break Statement
print("\nThe break Statement")
for x in fruits:
  print(x)
  if x == "banana":
    break