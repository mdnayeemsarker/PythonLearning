#Python Tuples

#Tuple
print("\nTuple")
mytuple = ("apple", "banana", "cherry")
print(mytuple)


#Allow Duplicates
print("\nAllow Duplicates")

mytuple = ("apple", "banana", "cherry", "apple", "cherry")
print(mytuple)


#Tuple Length
print("\nTuple Length")
print(len(mytuple))


#Create Tuple With One Item
print("\nCreate Tuple With One Item")
mytupleo = ("apple",)
mytupleo1 = ("apple")
print(type(mytupleo))
print(type(mytupleo1))